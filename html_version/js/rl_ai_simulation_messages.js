/**
 * 랜덤하게 메시지를 선택하여 반환
 */
function getRandomSimulationMessage() {
    const randomIndex = Math.floor(Math.random() * RLSimulationMessages.length);
    return RLSimulationMessages[randomIndex];
}

/**
 * 모의전 통계에 맞는 메시지를 반환
 * @param {Object} stats - 모의전 통계 객체
 * @returns {string} 메시지
 */
function getSimulationMessageForStats(stats) {
    if (!stats) {
        return "모의전 통계를 불러오는 중이에요. 잠시만 기다려주세요.";
    }
    
    const buyCount = stats.buy_count || 0;
    const sellCount = stats.sell_count || 0;
    const profit = stats.total_profit || 0;
    const loss = stats.total_loss || 0;
    const netProfit = stats.net_profit || 0;
    const winRate = stats.win_rate || 0;
    
    // 통계에 맞는 메시지 생성
    if (buyCount === 0 && sellCount === 0) {
        return "아직 모의전 거래가 없어요. AI가 거래를 시작하면 통계가 표시될 거예요.";
    }
    
    if (netProfit > 0 && winRate > 60) {
        return `모의전에서 ${buyCount}번 매수하고 ${sellCount}번 매도했어요. 순손익은 +${netProfit.toFixed(0)}원이고 승률은 ${winRate.toFixed(1)}%예요. 잘하고 있어요!`;
    }
    
    if (netProfit > 0) {
        return `모의전에서 ${buyCount}번 매수하고 ${sellCount}번 매도했어요. 순손익은 +${netProfit.toFixed(0)}원이에요.`;
    }
    
    if (netProfit < 0) {
        return `모의전에서 ${buyCount}번 매수하고 ${sellCount}번 매도했어요. 순손익은 ${netProfit.toFixed(0)}원이에요. 더 학습이 필요해요.`;
    }
    
    return `모의전에서 ${buyCount}번 매수하고 ${sellCount}번 매도했어요. 수익은 ${profit.toFixed(0)}원, 손실은 ${loss.toFixed(0)}원이에요.`;
}

